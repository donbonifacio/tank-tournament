#include "../include/Enemy.h"
#include "../include/Tank.h"

using namespace Tank;

//---------------------------------------------------------------------------------------------------------
void Enemy::moveTank( TankV* _tank )
{
	_tank->basicMove( getRandomDirection() );
}

//---------------------------------------------------------------------------------------------------------
void Enemy::init( TankV* _tank )
{
	_tank->setDirection( Direction::up() );
}

//---------------------------------------------------------------------------------------------------------
Direction* Enemy::getRandomDirection()
{
	int newDir = RND->get(4);
	Direction* direction = Direction::getDirection((Direction::Type)newDir);
	assert( 0 != direction );
	return direction;
}

